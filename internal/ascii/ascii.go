// ANSI Color Codes
const (
	Reset     = "\033[0m"
	Cyan      = "\033[36m"
	Blue      = "\033[34m"
	Magenta   = "\033[35m"
	Red       = "\033[31m"
	Yellow    = "\033[33m"
	Bold      = "\033[1m"
)

// PrintBanner affiche une des banni√®res al√©atoirement ou par s√©lection avec un beau d√©grad√©
func PrintBanner() {
	rand.Seed(time.Now().UnixNano())
	banners := []string{BannerBlocks, BannerClassic, BannerSlanted}
	selected := banners[rand.Intn(len(banners))]

	// Liste des d√©grad√©s de couleurs √† appliquer par ligne
	gradients := [][]string{
		{Cyan, Blue, Magenta},
		{Red, Yellow},
		{Blue, Cyan},
	}
	selectedGradient := gradients[rand.Intn(len(gradients))]

	lines := strings.Split(selected, "\n")
	for i, line := range lines {
		if strings.TrimSpace(line) == "" {
			continue
		}
		// Alternance ou interpolation simple de couleur sur la ligne
		color := selectedGradient[i%len(selectedGradient)]
		fmt.Printf("%s%s%s\n", color, line, Reset)
	}
	fmt.Println()
}
